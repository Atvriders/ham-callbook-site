# GET /api/gedcom/{cs}
# ---------------------------------------------------------------------------

def _build_gedcom(cs: str, rows: list[sqlite3.Row]) -> str:
    """Build a GEDCOM 5.5.1 string from archive rows for one callsign."""
    lines: list[str] = []

    now_iso = datetime.utcnow().strftime("%d %b %Y").upper()

    # Header
    lines += [
        "0 HEAD",
        "1 SOUR HamCallbookArchive",
        "2 NAME U.S. Amateur Radio Callbook Archive",
        "2 VERS 1909-2003",
        "1 GEDC",
        "2 VERS 5.5.1",
        "2 FORM LINEAGE-LINKED",
        "1 CHAR UTF-8",
        f"1 DATE {now_iso}",
        "1 NOTE Generated by hamcallbook.archive — verify all facts independently.",
    ]

    # Collect holders (group rows by normalised name)
    holder_rows: dict[str, list[sqlite3.Row]] = {}
    for r in rows:
        key = (r["name"] or "").strip().upper() or "(unknown)"
        holder_rows.setdefault(key, []).append(r)

    indi_idx = 0

    # Source record
    lines += [
        "0 @S1@ SOUR",
        "1 TITL U.S. Amateur Radio Callbook Archive",
        "1 AUTH Callbook Publications / CQ Communications",
        "1 PUBL Digitised archive 1909–2003; hamcallbook.archive",
    ]

    for holder_key, h_rows in holder_rows.items():
        indi_idx += 1
        xref = f"@I{indi_idx}@"
        h_rows_sorted = sorted(h_rows, key=lambda r: r["year"])

        raw_name = h_rows_sorted[0]["name"] or holder_key
        # Try to split into given/surname (callbook format is usually "LAST FIRST" or "FIRST LAST")
        # Treat as "given" only if we can't split; genealogy software handles it
        name_gedcom = raw_name.replace("/", "").strip()
        # Wrap surname in slashes if it looks like "LAST, FIRST" or is a single token
        parts = name_gedcom.split()
        if len(parts) >= 2:
            given = " ".join(parts[1:])
            surn = parts[0].rstrip(",")
            display_name = f"{given} /{surn}/"
        else:
            display_name = f"{name_gedcom}"

        lines += [
            f"0 {xref} INDI",
            f"1 NAME {display_name}",
        ]

        prev_class: str | None = None
        prev_cs: str | None = None

        for r in h_rows_sorted:
            yr = r["year"]
            edition = r["edition"] or str(yr)
            lclass = (r["license_class"] or "").strip().upper()
            addr_parts = [
                r["address"] or "",
                r["city"] or "",
                r["state"] or "",
                r["zip"] or "",
            ]
            addr = ", ".join(p for p in addr_parts if p.strip())

            # RESI event per edition-year
            lines += [
                "1 RESI",
                f"2 DATE {_ged_date(yr)}",
            ]
            if addr.strip():
                lines.append(f"2 ADDR {addr.strip()[:248]}")
            lines += [
                "2 SOUR @S1@",
                f"3 PAGE {edition} edition, callsign {cs}",
                f"3 NOTE License class: {lclass or 'unknown'}",
            ]

            # First license event on first row
            if r is h_rows_sorted[0]:
                lines += [
                    "1 EVEN",
                    f"2 TYPE First License",
                    f"2 DATE {_ged_date(yr)}",
                    f"2 NOTE Callsign {cs} first appeared in the {edition} callbook edition.",
                    "2 SOUR @S1@",
                    f"3 PAGE {edition} edition",
                ]

            # Upgrade event on class change
            if lclass and lclass != prev_class and prev_class is not None:
                lines += [
                    "1 EVEN",
                    f"2 TYPE License Upgrade",
                    f"2 DATE {_ged_date(yr)}",
                    f"2 NOTE Upgraded from {prev_class} to {lclass} (observed in {edition} edition).",
                    "2 SOUR @S1@",
                    f"3 PAGE {edition} edition",
                ]
            prev_class = lclass or prev_class

            # Callsign-change event (only relevant if prior holder was different callsign, not applicable here)
            # We emit callsign as a custom attribute on each INDI
            if cs != prev_cs:
                lines += [
                    "1 EVEN",
                    f"2 TYPE Amateur Radio Callsign",
                    f"2 DATE {_ged_date(yr)}",
                    f"2 NOTE Callsign: {cs}",
                    "2 SOUR @S1@",
                    f"3 PAGE {edition} edition",
                ]
                prev_cs = cs

    # Trailer
    lines.append("0 TRLR")

    return "\r\n".join(lines) + "\r\n"


@router.get("/{cs}")
def get_gedcom(
    cs: str = PathParam(..., description="Callsign, case-insensitive."),
    db: sqlite3.Connection = Depends(get_db),
) -> Response:
    """Emit a GEDCOM 5.5.1 file for a callsign's archive history."""
    callsign = normalize_callsign(cs)

    cur = db.execute(
        """
        SELECT year, edition, name, address, city, state, zip, license_class
        FROM   entries
        WHERE  callsign = ?
        ORDER  BY year ASC
        """,
        (callsign,),
    )
    rows = cur.fetchall()
    if not rows:
        raise HTTPException(status_code=404, detail=f"callsign not found: {callsign}")

    ged_text = _build_gedcom(callsign, rows)

    return Response(
        content=ged_text.encode("utf-8"),
        media_type="text/plain; charset=utf-8",
        headers={
            "Content-Disposition": f'attachment; filename="{callsign}.ged"',
            "Cache-Control": "no-store",
        },
    )


