"""GEDCOM Bridge — Feature #15.

Two endpoints:

  GET  /api/gedcom/{cs}   — emit a GEDCOM 5.5.1 .ged for a callsign/holder:
                             RESI events per edition-year address,
                             custom events for first-license / upgrades /
                             callsign-changes, per-fact SOUR citing the edition.
                             Returns text/plain with Content-Disposition .ged.

  POST /api/gedcom/scan   — upload a family-tree GEDCOM (max 5 MB, stateless,
                             never stored).  Parse INDI names, match against the
                             pre-built phonetic_index.json + state/era
                             plausibility.  Return conservative candidate hits
                             with confidence and explicit 'verify yourself'
                             framing.

GEDCOM 5.5.1 is a line-based text format:
    level TAG [value]
Lines are at most 255 chars; continuations use CONT.
"""

from __future__ import annotations

import io
import logging
import re
import sqlite3
from datetime import datetime
from typing import Any

from fastapi import APIRouter, Depends, HTTPException, Path as PathParam, UploadFile, File
from fastapi.responses import Response
from pydantic import BaseModel

from app.db import get_db
from app.routes.callsign import normalize_callsign
from app.integrations import phonetic_index as _phonetic_index

logger = logging.getLogger("callbook.backend.gedcom")

router = APIRouter(prefix="/api/gedcom", tags=["gedcom"])

_GEDCOM_MAX_BYTES = 5 * 1024 * 1024  # 5 MB

# ---------------------------------------------------------------------------
# Pydantic models
# ---------------------------------------------------------------------------


class GedcomCandidate(BaseModel):
    indi_xref: str
    tree_name: str
    callsign: str
    archive_name: str
    first_year: int
    last_year: int
    state: str | None
    confidence: str   # "low" | "medium" | "high"
    note: str


class GedcomScanResult(BaseModel):
    scanned_individuals: int
    candidates: list[GedcomCandidate]
    disclaimer: str


# ---------------------------------------------------------------------------
# GEDCOM line helpers
# ---------------------------------------------------------------------------

def _ged_line(level: int, tag: str, value: str = "") -> str:
    """Emit one GEDCOM line, capping at 248 payload chars (spec limit)."""
    if value:
        line = f"{level} {tag} {value}"
    else:
        line = f"{level} {tag}"
    # Long values: split with CONT
    if len(line) <= 255:
        return line
    # Hard truncate for GEDCOM safety (real apps use CONC/CONT splits)
    head = line[:248]
    rest = line[248:]
    out = [head]
    while rest:
        out.append(f"  CONT {rest[:248]}")
        rest = rest[248:]
    return "\n".join(out)


def _ged_date(year: int) -> str:
    """Approximate GEDCOM date for a year-only known date."""
    return f"ABT {year}"


