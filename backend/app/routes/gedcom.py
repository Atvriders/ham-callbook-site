# Parse INDI name lines from a GEDCOM upload
_INDI_RE = re.compile(r"^0\s+(@\S+@)\s+INDI", re.MULTILINE)
_NAME_RE = re.compile(r"^1\s+NAME\s+(.+)$", re.MULTILINE)
_BIRTH_RE = re.compile(r"^2\s+DATE\s+.*?(\d{4})", re.MULTILINE)


def _extract_individuals(text: str) -> list[dict[str, Any]]:
    """Parse GEDCOM text -> list of {xref, name, birth_year?}."""
    individuals: list[dict[str, Any]] = []
    # Split on INDI records
    blocks = re.split(r"(?=^0\s+@\S+@\s+INDI)", text, flags=re.MULTILINE)
    for block in blocks:
        m_indi = _INDI_RE.match(block.strip())
        if not m_indi:
            continue
        xref = m_indi.group(1)
        m_name = _NAME_RE.search(block)
        if not m_name:
            continue
        raw_name = m_name.group(1).strip()
        # GEDCOM name format: "Given /SURNAME/ Suffix" — strip slashes
        clean_name = re.sub(r"/([^/]*)/", r"\1", raw_name).strip()
        # Birth year: look for BIRT event then DATE
        birth_year: int | None = None
        birt_match = re.search(r"1\s+BIRT.*?(?=^1\s|\Z)", block, re.DOTALL | re.MULTILINE)
        if birt_match:
            yr_m = _BIRTH_RE.search(birt_match.group(0))
            if yr_m:
                birth_year = int(yr_m.group(1))
        # State: RESI ADDR lines
        state_hints: list[str] = []
        for addr_m in re.finditer(r"2\s+ADDR\s+(.+)", block):
            # Last two uppercase alpha tokens of an address line are often state abbreviations
            tokens = addr_m.group(1).split()
            for tok in reversed(tokens):
                tok_clean = tok.strip(",.").upper()
                if len(tok_clean) == 2 and tok_clean.isalpha():
                    state_hints.append(tok_clean)
                    break
        individuals.append(
            {
                "xref": xref,
                "name": clean_name,
                "birth_year": birth_year,
                "state_hints": list(set(state_hints)),
            }
        )
    return individuals


def _phonetic_key_for_name(name: str) -> list[str]:
    """Return candidate phonetic keys for a person name string."""
    # Import helpers from people route (same logic)
    try:
        import jellyfish  # type: ignore

        def _meta(w: str) -> str:
            return jellyfish.metaphone(w) or w[:4].upper()
    except ImportError:
        def _meta(w: str) -> str:  # type: ignore[misc]
            return w[:4].upper()

    tokens = [t for t in re.findall(r"[A-Za-z]+", name) if len(t) > 1]
    if not tokens:
        return []
    keys = []
    # Full name key (first + last)
    if len(tokens) >= 2:
        keys.append(f"{_meta(tokens[0])}|{_meta(tokens[-1])}")
    # Surname-only
    keys.append(_meta(tokens[-1]))
    # First-only
    keys.append(_meta(tokens[0]))
    return list(dict.fromkeys(keys))  # dedup, preserve order


def _confidence(name_match: bool, state_match: bool, era_match: bool) -> str:
    score = sum([name_match, state_match, era_match])
    if score == 3:
        return "high"
    if score == 2:
        return "medium"
    return "low"


@router.post("/scan", response_model=GedcomScanResult)
async def scan_gedcom(
    file: UploadFile = File(..., description="GEDCOM 5.5.1 family-tree file (max 5 MB)."),
) -> GedcomScanResult:
    """
    Upload a family-tree GEDCOM.  Match individuals against the callbook
    phonetic index.  Stateless — file processed in memory, never stored.
    Returns conservative candidate hits; all require manual verification.
    """
    raw = await file.read(_GEDCOM_MAX_BYTES + 1)
    if len(raw) > _GEDCOM_MAX_BYTES:
        raise HTTPException(
            status_code=413,
            detail="GEDCOM file exceeds 5 MB limit.",
        )

    try:
        text = raw.decode("utf-8", errors="replace")
    except Exception:
        raise HTTPException(status_code=400, detail="Could not decode GEDCOM file as UTF-8.")

    individuals = _extract_individuals(text)
    if not individuals:
        raise HTTPException(status_code=400, detail="No INDI records found in uploaded GEDCOM.")

    # Ensure phonetic index is loaded
    _phonetic_index.ensure_loaded()

    candidates: list[GedcomCandidate] = []

    for person in individuals[:500]:  # cap at 500 individuals per upload
        name = person["name"]
        birth_year: int | None = person["birth_year"]
        state_hints: list[str] = person["state_hints"]

        if not name or len(name) < 2:
            continue

        pkeys = _phonetic_key_for_name(name)

        seen_callsigns: set[str] = set()

        for pkey in pkeys:
            posting = _phonetic_index.lookup(pkey)
            if not posting:
                continue

            # posting shape: {t: [[callsign, year, name, state], ...], p: ...}
            entries: list[Any] = posting.get("t", [])

            for entry in entries[:200]:  # limit per key
                if len(entry) < 4:
                    continue
                e_callsign: str = entry[0] if entry[0] else ""
                e_year: int = int(entry[1]) if entry[1] else 0
                e_name: str = entry[2] if entry[2] else ""
                e_state: str = (entry[3] or "").strip().upper()

                if not e_callsign or e_callsign in seen_callsigns:
                    continue

                # Era plausibility: birth_year within +-40 yrs of first license
                era_ok = True
                if birth_year and e_year:
                    age_at_license = e_year - birth_year
                    era_ok = 10 <= age_at_license <= 80  # plausible licensing age range

                # State match
                state_ok = not state_hints or e_state in state_hints

                conf = _confidence(True, state_ok, era_ok)

                # Only include medium/high confidence matches
                if conf == "low" and not state_ok and not era_ok:
                    continue

                # Build a human-readable note
                note_parts = [
                    f"Your tree individual '{name}' may match callbook operator "
                    f"'{e_name}' who held {e_callsign} in {e_year}",
                ]
                if e_state:
                    note_parts.append(f"(state: {e_state})")
                note_parts.append("— verify independently before drawing conclusions.")
                note = " ".join(note_parts)

                seen_callsigns.add(e_callsign)

                # Find first and last year for this callsign in the index
                # (approximate: use e_year as both; fuller lookup would hit DB)
                candidates.append(
                    GedcomCandidate(
                        indi_xref=person["xref"],
                        tree_name=name,
                        callsign=e_callsign,
                        archive_name=e_name,
                        first_year=e_year,
                        last_year=e_year,
                        state=e_state or None,
                        confidence=conf,
                        note=note,
                    )
                )

                if len(candidates) >= 200:
                    break
            if len(candidates) >= 200:
                break

    # Sort: high > medium > low, then by confidence label
    _conf_order = {"high": 0, "medium": 1, "low": 2}
    candidates.sort(key=lambda c: _conf_order.get(c.confidence, 9))

    return GedcomScanResult(
        scanned_individuals=len(individuals),
        candidates=candidates[:100],  # return at most 100
        disclaimer=(
            "These are candidate matches only — phonetic name similarity does not confirm identity. "
            "Always verify against primary sources (FCC ULS, QSL cards, obituaries, family records) "
            "before including in a genealogical record."
        ),
    )
